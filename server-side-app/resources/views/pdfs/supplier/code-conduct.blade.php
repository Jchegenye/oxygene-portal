                <p>The Company expects all its Suppliers to observe the highest standards of ethics and integrity in their business conduct and has set the minimum acceptable standards with respect to Suppliers’ operations and dealings with the Company. The Supplier agrees to abide by the terms of this Code and acknowledge that compliance with this Code is required to maintain the Supplier’s relationship with the Company. The Company shall monitor compliance with this Code and shall have the right to terminate any Supplier contract for failure by the Supplier to comply with the provisions of this Code.</p>

                <h6 class="heading6">Compliance with the Law:</h6><p>The Supplier is required to comply with all applicable laws, regulations and codes of business practice in the countries in which it operates.</p>

                <h6 class="heading6">Fair Competition:</h6><p> The Company believes in competitive and fair market play. The Supplier is required to act in a manner that will uphold and encourage healthy competition and to comply with applicable competition laws.</p>
                <h6 class="heading6">Labour Relations: </h6><p>The Supplier is required to comply with all applicable laws relating to working hours, employment rights, employee health and safety working conditions and wages. The Supplier will not use any form of forced or compulsory labour. The Supplier will adhere to all applicable laws relating to the employment or engagement of children in all countries in which its operates or sources goods. The Supplier shall ensure that it does not employ discriminatory practices against any worker on any ground, including race, religion, disability or gender. The Supplier shall not engage in or condone the use of corporal punishment, mental, physical, sexual or verbal abuse or any cruel or unprocedural disciplinary practices in the workplace.</p>
                <h6 class="heading6">Inducement:</h6><p>The Supplier will not make or offer to make any inducement, monetary or otherwise, to any employee of the Company with a view of influencing that employee’s business judgment. The Supplier confirms that no employee of the Company has received or will be offered by the Supplier or any agent of the Supplier, any direct or indirect benefit arising from the Supplier’s relationship with the Company. A Supplier shall immediately report to the Company if any inducement is solicited from it by an employee
                    <br/><br/>or agent of the Company or if it believes that any employee or anyone acting on behalf of the Company or any other supplier of the Company has engaged in illegal or other- wise improper conduct in their dealings with the Company.</p>
            </td>
            <td class="w-50">
                <p>Such report should be made in confidence to the Group Company Secretary & Head of Legal, Oxygene Limited. A Supplier’s relationship with the Company will not be affected by such report made in good faith.</p>
                <h6 class="heading6">Conflict of Interest / Gifts: </h6><p>The Company requires that any of its employees who may have a personal relationship with a Supplier to disclose the same to the Company. Similarly, a Supplier who is a family relation (spouse, partner, child, parent or sibling) of an employee of the Company or who has any relationship with an employee of the Company or the Company itself, which might present a conflict of interest or be perceived to present a conflict of interest should disclose the fact to the Company or ensure that the Company employee does so. Company employees are required to declare all gifts and entertainment received from the Company’s clients and suppliers. The Supplier is required to ensure that any gift or expenditure incurred in relation to any Company employee is in the ordinary and proper course of business and cannot reasonably be construed as a bribe or inducement to secure unfair preferential treatment.</p>
                <h6 class="heading6">Corruption:</h6><p>The Company has a zero tolerance to corruption and the Supplier shall in all its dealings with the Company act in an ethical manner. In particular, the Supplier shall comply with the Kenya Anti-Corruption and Social Economic Crimes Act 2003 as may be modified from time to time as well as any other legislation in force from time to time relating to corrupt practices.</p>
                <h6 class="heading6">The Environment:</h6><p>The Supplier is required to comply with all local environmental laws in force from time to time and shall implement programmes geared towards promoting a clean environment and reducing waste. The Supplier is required to identify all hazardous or toxic waste that it or its contractors/ agents produce and confirm that it is satisfied that any such waste is disposed of via authorized disposal routes.</p>
                <h6 class="heading6">Legal Standing and Financial Integrity: </h6><p>The Supplier is required to obtain and renew, in accordance with any law or regulation any permits and licences required to carry out its business. The Supplier shall maintain accurate and reliable financial and operational records related to the services or goods delivered to the Company</p>
                <h6 class="heading6">Audit:</h6><p>The Supplier agrees to facilitate an audit by the Company or agent of the Company of its facilities and records used in the provision of the services or goods delivered to the Company, to ascertain compliance with this Code.</p>
                <h6 class="heading6">Amendment of the Code:</h6><p>The Company shall be entitled to amend this Code from time to time as it deems necessary and shall communicate such amendments to the Supplier</p>
