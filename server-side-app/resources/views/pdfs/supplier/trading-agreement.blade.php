                    <li><span class="numbering">4.4</span>The Supplier shall raise any queries arising from the purchase order prior to the execution of the purchase order</li>
                    <li><span class="numbering">4.5</span>Once the purchase order has been accepted, the Supplier shall be bound by the terms thereof.</li>
                    <li><span class="numbering">4.6</span>Substitute or extra charges under the purchase order will not be permitted except on specific authority from the Company evidenced by issuance of a supplementary purchase order</li>
