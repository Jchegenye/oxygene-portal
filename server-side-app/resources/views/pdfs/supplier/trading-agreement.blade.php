                    <li><span class="numbering">3.7</span>The Company will endeavor to pay the Supplier promptly and will do so provided services or goods are delivered in accordance with the purchase order and all invoices/ statements/documentation are correct and reach the Company before the 25th of the following month. Any invoice reaching the Company after that date will only be processed for payment in the next payment cycle.</li>
                    <li><span class="numbering">3.8</span>The Supplier shall refund any amounts paid by the Company in error immediately.</li>
