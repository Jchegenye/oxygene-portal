                <h6 class="heading6">Definitions</h6>
                <p><strong>“Oxygene”</strong> means Oxygene Marketing Communication Limited referred to as the <strong>“Company”</strong>.</p>
                <p><strong>“Supplier”</strong> means the person or entity providing goods or services to the Company.</p>

                <h6 class="heading6">1. Opening Supplier Account</h6>
                <ol>
                    <li><span class="numbering">1.1</span> A supplier account will only be opened upon submission of the required documents as stipulated in the supplier form under clause 4. Evaluation requirements.:</li>
                    <li><span class="numbering">1.2</span> Delivery of goods or performance of services prior to receipt of the above documents may result in non-payment to the Supplier</li>
                </ol>

                <h6 class="heading6">2. Trading Terms</h6>
                <ol>
                    <li><span class="numbering">2.1</span> The Company’s terms of payment are generally 60 days from the statement date for approved invoices subject to any other terms specified in the purchase order.</li>
                    <li><span class="numbering">2.2</span>Invoices must be supported by a copy of the purchase order and delivery note. Failure to do so may result in the Company being unable to process the relevant invoice for payment in a timely manner.</li>
                    <li><span class="numbering">2.3</span>The maximum price of the goods will be as indicated in the purchase order and will not be subject to escalation.</li>
                    <li><span class="numbering">2.4</span>No invoice will be paid if there is a price difference between the purchase order and the invoice amount and the total value of the invoice will be withheld until the query is resolved and or credit note is received.</li>
                    <li><span class="numbering">2.5</span>The Supplier shall ensure that all government taxes, levies or duties have been levied in respect of all goods and services and that any quality assurance or standardization mark required for such goods or services is obtained. The Supplier shall indemnify the Company against any claim, loss, costs, or proceedings arising out of breach of the Supplier’s obligation herein.</li>
                </ol>

                <h6 class="heading6">3. Account Payment</h6>
                <ol>
                    <li><span class="numbering">3.1</span>Any payment that falls due on a Saturday, Sunday or Public Holiday will be effected on the first working day thereafter.</li>
                    <li><span class="numbering">3.2</span>Payment will be made by cheque or RTGS at the Supplier’s nominated bank account.</li>
                    <li><span class="numbering">3.3</span>Disputed invoices must be resolved within 1 month of query being raised.</li>
                </ol>
